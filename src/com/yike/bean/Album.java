package com.yike.bean;

public class Album {

	private String albumId;
	private String channelName;
	private String name;
	private String englishName;
	private String directorNames;
	private String starringNames;
	private String hostNames;
	private String age;
	private String standardPic;
	private String ystandardPic;
	private String categoryNames;
	private String tag;
	private String score;
	private String createTime;

	public String getAlbumId() {
		return albumId;
	}

	public void setAlbumId(String albumId) {
		this.albumId = albumId;
	}

	public String getChannelName() {
		return channelName;
	}

	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEnglishName() {
		return englishName;
	}

	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}

	public String getDirectorNames() {
		return directorNames;
	}

	public void setDirectorNames(String directorNames) {
		this.directorNames = directorNames;
	}

	public String getStarringNames() {
		return starringNames;
	}

	public void setStarringNames(String starringNames) {
		this.starringNames = starringNames;
	}

	public String getHostNames() {
		return hostNames;
	}

	public void setHostNames(String hostNames) {
		this.hostNames = hostNames;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getStandardPic() {
		return standardPic;
	}

	public void setStandardPic(String standardPic) {
		this.standardPic = standardPic;
	}

	public String getYstandardPic() {
		return ystandardPic;
	}

	public void setYstandardPic(String ystandardPic) {
		this.ystandardPic = ystandardPic;
	}

	public String getCategoryNames() {
		return categoryNames;
	}

	public void setCategoryNames(String categoryNames) {
		this.categoryNames = categoryNames;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

}